    public void settable(int Tabletype){
        DefaultTableModel dtm = (DefaultTableModel) GameTable.getModel();
        for(int i = 0;i < dtm.getRowCount();i++){
            dtm.removeRow(i);
            i--;
        }
        try {
            games.makeGames(Tabletype);
        }
        catch (FileNotFoundException ex) {
            Logger.getLogger(ProjectFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        glist = games.getGames();
        for(Info game:glist){
            Object[] entry = {game.getTitle(),game.getESRB(),game.getGenre(),game.getRating()};
            dtm.addRow(entry);
        }
        InfoButton.setEnabled(false);
    }
