    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        ResetButtonActionPerformed(evt);
        if(Searchbox.getText().equals("") != true){
            int x,y,keep = 0;
            String value;
            DefaultTableModel dtm = (DefaultTableModel) GameTable.getModel();
            for(x = 0; x < dtm.getRowCount(); x++){
                for(y = 0; y < dtm.getColumnCount(); y++){
                    value = dtm.getValueAt(x, y).toString();
                    if(value.toLowerCase().contains(Searchbox.getText().toLowerCase()) == true){
                        keep = 1;
                    }
                }
                if (keep == 1){
                    keep = 0;
                }
                else{
                    dtm.removeRow(x);
                    x = x-1;
                }
            }
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

    private void AdSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdSearchActionPerformed
        int x,keep = 0;
        DefaultTableModel dtm = (DefaultTableModel) GameTable.getModel();
        for(x = 0; x < dtm.getRowCount(); x++){
            if (Title.getText().equals("") != true && dtm.getValueAt(x, 0).toString().toLowerCase().contains(Title.getText().toLowerCase()) == true){
                keep = 1;
            }
            if (ESRB.getText().equals("") != true && dtm.getValueAt(x, 1).toString().toLowerCase().contains(ESRB.getText().toLowerCase()) == true){
                keep = 1;
            }
            if (Genre.getText().equals("") != true && dtm.getValueAt(x, 2).toString().toLowerCase().contains(Genre.getText().toLowerCase()) == true){
                keep = 1;
            }
            if (Rating.getText().equals("") != true && Rating.getText().equals(dtm.getValueAt(x, 3).toString()) == true){
                keep = 1;
            }
            if (keep == 1){
                keep = 0;
            }
            else{
                dtm.removeRow(x);
                x = x-1;
            }
        }
        Title.setText("");
        ESRB.setText("");
        Genre.setText("");
        Rating.setText("");
        AdvancedSearch.setVisible(false);
    }//GEN-LAST:event_AdSearchActionPerformed

